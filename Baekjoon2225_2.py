import sys

sys.stdin = open("Baekjoon2225.txt")
N, K = map(int, input().split())
dp = [[0] * N for _ in range(K)]
dp[0] = [1] * N
for y in range(1, K):
    dp[y][0] = (dp[y - 1][0] + 1) % 1000000000
    for x in range(1, N):
        dp[y][x] = (dp[y - 1][x] + dp[y][x - 1]) % 1000000000
print(dp[-1][-1])

