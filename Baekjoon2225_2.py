def cho(paArr):
    if sum(paArr) > N:
        return
    if len(paArr) == K:
        if sum(paArr) == N:
            global a
            print(paArr)
            a += 1
        return
    for i in range(N + 1):
        cho(paArr + [i])


N, K = map(int, input().split())
a = 0
cho([])
print(a)

